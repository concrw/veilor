def sb_request(method, path, body=None, extra_headers=None):
    url = f"{SUPABASE_URL}/rest/v1/{path}"
    headers = {
        "apikey": SUPABASE_SERVICE_ROLE_KEY,
        "Authorization": f"Bearer {SUPABASE_SERVICE_ROLE_KEY}",
        "Accept": "application/json",
        "Accept-Profile": "veilor",
        "Content-Profile": "veilor",
    }
    if extra_headers:
        headers.update(extra_headers)
    data = json.dumps(body).encode() if body else None
    if data:
        headers["Content-Type"] = "application/json"
    req = urllib.request.Request(url, data=data, headers=headers, method=method)
    try:
        with urllib.request.urlopen(req, timeout=15) as r:
            content = r.read()
            return r.status, json.loads(content) if content else []
    except urllib.error.HTTPError as e:
        body_text = e.read().decode()[:300]
        return e.code, {"error": body_text}


def upsert_vectorize(vectors):
    data = json.dumps({"vectors": vectors}).encode()
    headers = {"Authorization": f"Bearer {CF_API_TOKEN}", "Content-Type": "application/json"}
    req = urllib.request.Request(
        f"{VECTORIZE_URL}/upsert", data=data, headers=headers, method="POST"
    )
    try:
        with urllib.request.urlopen(req, timeout=15) as r:
            return r.status in (200, 201)
    except Exception as e:
        print(f"  Vectorize error: {e}")
        return False


def main():
    if not SUPABASE_SERVICE_ROLE_KEY:
        print("ERROR: SUPABASE_SERVICE_ROLE_KEY 미설정")
        sys.exit(1)
    if not CF_ACCOUNT_ID or not CF_API_TOKEN:
        print("ERROR: CF_ACCOUNT_ID / CF_API_TOKEN 미설정")
        sys.exit(1)

    print("KURE-v1 로컬 모델 로드 중...")
    from sentence_transformers import SentenceTransformer
    model = SentenceTransformer("nlpai-lab/KURE-v1")
    print(f"모델 로드 완료 (dim={model.get_sentence_embedding_dimension()})\n")

    print(f"[psych_qa → sex_qa 테이블] {len(QA_DATA)}개 Q&A 삽입 시작")
    ok_count = 0
    skip_count = 0

    for i, qa in enumerate(QA_DATA, 1):
        status, resp = sb_request("POST", "sex_qa", qa, {"Prefer": "return=representation"})

        if status == 409:
            print(f"  ○ [{i}/{len(QA_DATA)}] 중복 스킵: {qa['question'][:55]}")
            skip_count += 1
            continue

        if status not in (200, 201) or not isinstance(resp, list) or not resp:
            print(f"  FAIL [{status}] {qa['question'][:55]}: {resp}")
            continue

        row_id = resp[0]["id"]
        text = f"{qa['question']}\n{qa['answer']}"
        vec = model.encode([text[:512]], show_progress_bar=False)[0].tolist()

        ok = upsert_vectorize([{
            "id": row_id,
            "values": vec,
            "metadata": {
                "type": "sex_qa",
                "category": qa["category"],
                "domain_codes": qa["domain_codes"],
            },
        }])

        if ok:
            sb_request(
                "PATCH",
                f"sex_qa?id=eq.{row_id}",
                {"vectorized": True},
                {"Prefer": "return=minimal"},
            )
            ok_count += 1
            domain = qa["domain_codes"][0] if qa["domain_codes"] else "?"
            print(f"  ✓ [{ok_count}] [{domain}] {qa['question'][:55]}")
        else:
            print(f"  ○ 삽입됨 (벡터화 실패): {qa['question'][:50]}")

    print(f"\n{'='*60}")
    print(f"완료 — 신규 삽입·벡터화: {ok_count}개, 중복 스킵: {skip_count}개")
    print(f"전체 Q&A: {len(QA_DATA)}개")


if __name__ == "__main__":
    main()
